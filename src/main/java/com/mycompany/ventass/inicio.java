    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
modeloTabla3.setColumnIdentifiers(new Object[]{"Numero vendedor", "Fecha", "Producto", "Total"});
ventasPorDebito.setModel(modeloTabla3);
modeloTabla3.setRowCount(0);

ArrayList<Venta> listarVentasPorDebito = new ArrayList<>();
for (Venta v : listarVentas) {
    if (v.getFormaPago() == 1) {
        listarVentasPorDebito.add(v);
    }
}

for (Venta v : listarVentasPorDebito) {
    Producto precioU = null;
    for (Producto p : listarProductos) {
        if (p.getCodigo() == v.getCodigoProducto()) {
            precioU = p;
            break;
        }
    }

    if (precioU != null) {
        modeloTabla3.addRow(new Object[]{
                v.getCodigoVendedor(), v.getFechaVenta(),
                v.getCodigoProducto(), v.getPrecioFinal(precioU.getPrecioUnitario())
        });
    }
}

// Usar un HashMap para almacenar las ventas de cada vendedor
Map<Integer, Double> ventasPorVendedor = new HashMap<>();
Venta ventaMayorConTarjeta = null;

for (Venta venta : listarVentas) {
    Producto precioU = null;
    for (Producto p : listarProductos) {
        if (p.getCodigo() == venta.getCodigoProducto()) {
            precioU = p;
            break;
        }
    }

    if (precioU != null) {
        double precioFinal = venta.getPrecioFinal(precioU.getPrecioUnitario());
        
        // Registrar ventas por vendedor
        ventasPorVendedor.put(venta.getCodigoVendedor(),
                ventasPorVendedor.getOrDefault(venta.getCodigoVendedor(), 0.0) + precioFinal);

        // Determinar la venta de mayor importe con tarjeta de cr√©dito
        if (venta.getFormaPago() == 2) {
            if (ventaMayorConTarjeta == null || precioFinal > ventaMayorConTarjeta.getPrecioFinal(precioU.getPrecioUnitario())) {
                ventaMayorConTarjeta = venta;
            }
        }
    }
}

// Encontrar el mejor vendedor
int mejorVendedor = -1;
double maxVentas = 0;

for (Map.Entry<Integer, Double> entry : ventasPorVendedor.entrySet()) {
    if (entry.getValue() > maxVentas) {
        maxVentas = entry.getValue();
        mejorVendedor = entry.getKey();
    }
}

// Calcular la suma de ventas del mejor vendedor
double suma = ventasPorVendedor.getOrDefault(mejorVendedor, 0.0);
System.out.println("El vendedor con m√°s ventas es: " + mejorVendedor + " con " + suma);

// Mostrar la venta mayor con pago por tarjeta
if (ventaMayorConTarjeta != null) {
    System.out.println("Venta mayor con tarjeta de cr√©dito:");
    System.out.println("Fecha: " + ventaMayorConTarjeta.getFechaVenta());
    System.out.println("Vendedor: " + ventaMayorConTarjeta.getCodigoVendedor());
    System.out.println("Producto: " + ventaMayorConTarjeta.getCodigoProducto());

    Producto precioU = null;
    for (Producto p : listarProductos) {
        if (p.getCodigo() == ventaMayorConTarjeta.getCodigoProducto()) {
            precioU = p;
            break;
        }
    }

    if (precioU != null) {
        System.out.println("Total: " + ventaMayorConTarjeta.getPrecioFinal(precioU.getPrecioUnitario()));
    }
} else {
    System.out.println("No se registraron ventas con tarjeta de cr√©dito.");
}

textoVendedorMasAlto.setText(mejorVendedor+"");
textoMontoTotalVendedorMasAlto.setText(suma+"");
textoVentaMayorTarjeta.setText(ventaMayorConTarjeta.getCodigoVendedor()+"");
System.out.println("El vendedor con m√°s ventas es: " + mejorVendedor + " con " + suma);

    }//GEN-LAST:event_jButton3ActionPerformed

