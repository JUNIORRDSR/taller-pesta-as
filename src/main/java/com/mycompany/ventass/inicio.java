    private String obtenerRadioSeleccionado() {
        for (Enumeration<AbstractButton> buttons = botones1.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();
            if (button.isSelected()) {
                return button.getText();
            }
        }
        return null;
    }
    
    
